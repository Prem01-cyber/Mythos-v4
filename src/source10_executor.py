def load_existing(path: str) -> set[str]:
    """Return set of (type, tool) strings already written."""
    seen = set()
    p = Path(path)
    if not p.exists():
        return seen
    for line in p.read_text().splitlines():
        try:
            d = json.loads(line)
            seen.add(f"{d.get('type','')}::{d.get('tool','')}")
        except Exception:
            pass
    return seen


def count_by_type(path: str) -> dict[str, int]:
    counts: dict[str, int] = {}
    p = Path(path)
    if not p.exists():
        return counts
    for line in p.read_text().splitlines():
        try:
            d = json.loads(line)
            t = d.get("type", "unknown")
            counts[t] = counts.get(t, 0) + 1
        except Exception:
            pass
    return counts


write_lock = threading.Lock()


def append_jsonl(path: str, examples: list[dict]) -> int:
    if not examples:
        return 0
    with write_lock:
        with open(path, "a") as f:
            for ex in examples:
                f.write(json.dumps(ex) + "\n")
    return len(examples)


