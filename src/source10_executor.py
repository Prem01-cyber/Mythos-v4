def _parse_json_array(raw: str) -> list[dict]:
    """Extract a JSON array from GPT output robustly."""
    # strip markdown fences
    raw = re.sub(r"```(?:json)?", "", raw).strip().rstrip("`").strip()
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        # try to find the first [...] block
        m = re.search(r"\[.*\]", raw, re.DOTALL)
        if m:
            try:
                return json.loads(m.group(0))
            except Exception:
                pass
    return []


def generate_correction_examples(tool: str, help_text: str, n: int) -> list[dict]:
    if not help_text:
        return []
    prompt = CORRECTION_GEN_PROMPT.format(
        tool=tool, help_text=help_text[:2000], n=n
    )
    try:
        raw = _gpt(prompt, max_tokens=3000)
        items = _parse_json_array(raw)
    except Exception:
        return []

    results = []
    for item in items:
        if not all(k in item for k in ("bad_command", "stderr", "thought", "corrected")):
            continue
        user_content = (
            f"Tool: {tool}\n"
            f"Command attempted: {item['bad_command']}\n\n"
            f"Error output:\n{item['stderr']}\n\n"
            f"Tool help (excerpt):\n{help_text[:800]}\n\n"
            f"Fix this command."
        )
        asst_content = (
            f"<thought>{item['thought']}</thought>\n\n"
            f"<corrected>{item['corrected']}</corrected>"
        )
        results.append({
            "type": "correction",
            "tool": tool,
            "mistake_type": item.get("mistake_type", "unknown"),
            "messages": [
                {"role": "system",    "content": SYSTEM_CORRECTION},
                {"role": "user",      "content": user_content},
                {"role": "assistant", "content": asst_content},
            ],
        })
    return results


def generate_filtering_examples(tool: str, n: int) -> list[dict]:
    use_case = TOOL_USE_CASES.get(tool, "security scanning")
    prompt = FILTERING_GEN_PROMPT.format(tool=tool, use_case=use_case, n=n)
    try:
        raw = _gpt(prompt, max_tokens=3000)
        items = _parse_json_array(raw)
    except Exception:
        return []

    results = []
    for item in items:
        if not all(k in item for k in ("raw_output", "filtered_findings", "summary")):
            continue
        user_content = (
            f"Tool: {tool}\n"
            f"Raw output:\n```\n{item['raw_output']}\n```\n\n"
            f"Extract the findings. Strip progress bars, ANSI codes, repeated headers, and noise."
        )
        asst_content = (
            f"FINDINGS:\n{item['filtered_findings']}\n\n"
            f"SUMMARY: {item['summary']}"
        )
        results.append({
            "type": "filtering",
            "tool": tool,
            "messages": [
                {"role": "system",    "content": SYSTEM_FILTERING},
                {"role": "user",      "content": user_content},
                {"role": "assistant", "content": asst_content},
            ],
        })
    return results


