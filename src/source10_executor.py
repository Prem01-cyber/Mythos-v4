def detect_installed_tools() -> list[str]:
    return [t for t in PENTEST_TOOLS if shutil.which(t)]


def fetch_help(tool: str) -> str:
    """Capture --help output; fall back to -h; truncate to 3000 chars."""
    cache_path = Path(CACHE_DIR) / f"{tool}.help"
    if cache_path.exists():
        return cache_path.read_text()

    Path(CACHE_DIR).mkdir(parents=True, exist_ok=True)
    text = ""
    for flag in ["--help", "-h", "help"]:
        try:
            r = subprocess.run(
                [tool, flag],
                capture_output=True, text=True, timeout=10
            )
            out = (r.stdout + r.stderr).strip()
            if len(out) > 100:
                text = out[:3000]
                break
        except Exception:
            continue

    cache_path.write_text(text)
    return text


