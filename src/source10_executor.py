def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--test",            action="store_true")
    parser.add_argument("--test-n",          type=int, default=2)
    parser.add_argument("--list-categories", action="store_true")
    parser.add_argument("--type",            choices=["correction", "filtering", "both"], default="both")
    parser.add_argument("--workers",         type=int, default=DEFAULT_WORKERS)
    parser.add_argument("--out-correction",  default=OUTPUT_CORRECTION)
    parser.add_argument("--out-filtering",   default=OUTPUT_FILTERING)
    args = parser.parse_args()

    installed = detect_installed_tools()
    not_installed = [t for t in PENTEST_TOOLS if t not in installed]

    if args.list_categories:
        print(f"\n{'Tool':<20} {'Installed':<12} {'Help?':<8}")
        print("-" * 42)
        for t in PENTEST_TOOLS:
            inst  = t in installed
            help_ = Path(CACHE_DIR, f"{t}.help").exists() if inst else False
            print(f"  {t:<18} {'yes' if inst else 'no':<12} {'cached' if help_ else ''}")
        print(f"\nInstalled: {len(installed)} / {len(PENTEST_TOOLS)}")
        print(f"Correction file: {count_by_type(args.out_correction)}")
        print(f"Filtering  file: {count_by_type(args.out_filtering)}")
        return

    # ── TEST MODE ────────────────────────────────────────────────────────────
    if args.test:
        print("=" * 70)
        print("TEST MODE — source10_executor  (no file writes)")
        print("=" * 70)

        print(f"\nDetected {len(installed)} installed tools: {installed}")
        print(f"Missing:  {not_installed[:10]}{'...' if len(not_installed) > 10 else ''}")

        sample_tools = installed[:args.test_n] if installed else ["curl"]
        for tool in sample_tools:
            print(f"\n{'─'*60}")
            print(f"TOOL: {tool}")
            help_text = fetch_help(tool)
            print(f"  Help length : {len(help_text)} chars")
            print(f"  Help preview: {help_text[:200]!r}")

            print(f"\n  [A] Generating {args.test_n} correction example(s)...")
            corr = generate_correction_examples(tool, help_text, args.test_n)
            if corr:
                ex = corr[0]
                print(f"  Mistake type: {ex.get('mistake_type')}")
                print(f"  User msg    : {ex['messages'][1]['content'][:300]!r}")
                print(f"  Asst msg    : {ex['messages'][2]['content'][:300]!r}")
            else:
                print("  FAILED to generate correction examples")

            print(f"\n  [B] Generating {args.test_n} filtering example(s)...")
            filt = generate_filtering_examples(tool, args.test_n)
            if filt:
                ex = filt[0]
                print(f"  User msg    : {ex['messages'][1]['content'][:300]!r}")
                print(f"  Asst msg    : {ex['messages'][2]['content'][:300]!r}")
            else:
                print("  FAILED to generate filtering examples")

        print("\n" + "=" * 70)
        print("TEST COMPLETE — run without --test to generate full dataset")
        return

    # ── FULL RUN ─────────────────────────────────────────────────────────────
    Path(args.out_correction).parent.mkdir(parents=True, exist_ok=True)

    if not installed:
        print("No pentest tools detected in PATH. Install tools first.")
        return

    print(f"Found {len(installed)} installed tools. Generating training data...")
    total_written = 0

    def _worker_correction(tool: str) -> int:
        help_text = fetch_help(tool)
        if not help_text:
            return 0
        examples = generate_correction_examples(tool, help_text, EXAMPLES_PER_TOOL)
        return append_jsonl(args.out_correction, examples)

    def _worker_filtering(tool: str) -> int:
        examples = generate_filtering_examples(tool, FILTER_PER_TOOL)
        return append_jsonl(args.out_filtering, examples)

    if args.type in ("correction", "both"):
        corr_counts = count_by_type(args.out_correction)
        existing = corr_counts.get("correction", 0)
        remaining_tools = installed if existing < TARGET_CORRECTION else []
        print(f"\n[A] Correction  {existing}/{TARGET_CORRECTION} done")
        with ThreadPoolExecutor(max_workers=args.workers) as ex:
            futs = {ex.submit(_worker_correction, t): t for t in remaining_tools}
            for fut in tqdm(as_completed(futs), total=len(futs), desc="correction"):
                total_written += fut.result()

    if args.type in ("filtering", "both"):
        filt_counts = count_by_type(args.out_filtering)
        existing = filt_counts.get("filtering", 0)
        remaining_tools = installed if existing < TARGET_FILTERING else []
        print(f"\n[B] Filtering   {existing}/{TARGET_FILTERING} done")
        with ThreadPoolExecutor(max_workers=args.workers) as ex:
            futs = {ex.submit(_worker_filtering, t): t for t in remaining_tools}
            for fut in tqdm(as_completed(futs), total=len(futs), desc="filtering"):
                total_written += fut.result()

    print(f"\nWrote {total_written} new examples total")
    print(f"  Correction: {args.out_correction}")
    print(f"  Filtering:  {args.out_filtering}")


if __name__ == "__main__":
    main()
